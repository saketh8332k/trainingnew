//increment
//post increment
let a = 10;
console.log(a);
console.log(a++);
console.log(a++);

//pre-increment
let b = 20;
console.log(b);
console.log(++b);
console.log(++b);

//decrement
//post decrement
let c = 30;
console.log(c);
console.log(c--);
console.log(c--);

//pre-decrement
let d = 40;
console.log(d);
console.log(--d);
console.log(--d);